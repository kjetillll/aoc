my @r=map[/-?\d+/g],<>;
my $w=@r==12?11:101;
my $h=@r==12?7:103;
for(@r){
    $$_[0]+=($$_[2]*100);$$_[0]%=$w;
    $$_[1]+=($$_[3]*100);$$_[1]%=$h;
}
for(@r){
    $q=$$_[0] < $w>>1 && $$_[1] < $h>>1 ? 0
      :$$_[0] > $w>>1 && $$_[1] < $h>>1 ? 1
      :$$_[0] < $w>>1 && $$_[1] > $h>>1 ? 2
      :$$_[0] > $w>>1 && $$_[1] > $h>>1 ? 3 : next;
    $f[$q]++;
}
print "Answer: ".eval join '*',@f;
